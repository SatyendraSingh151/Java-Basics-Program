package Primitive_Data_Type;



public class Primitive_Data_Types_In_Java {

	public static void main(String[] args) {
		// this is the example of the primitive  data type in java programming

//		byte 
		byte b =1;
		System.out.println("The byte type data  is : "+b);
		
//		short type data
		short s = 151;
		System.out.println("The short type data is : "+s);

//		int data type
		int i = 4124;
		System.out.println("The int type data is : "+i);
		
//		long data type
		long l = 8004560467l;
		System.out.println("The long type data is : "+l);
		
//		float data type
		float f = 1245.45f;
		System.out.println("The float type data is : "+f);
		
//		double data type
		double d = 45.7895;
		System.out.println("The double type data is : "+d);
		
//		boolean data 
		boolean bool1 = true;
		System.out.println("The boolean type data is : "+bool1);
		

		boolean bool2 = false;
		System.out.println("The boolean type data is : "+bool2);
		
//		String data
		String str = "Satya Yadav";
		System.out.println("The String type data is : "+str);
	}

}

